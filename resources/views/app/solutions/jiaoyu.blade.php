            <p class=MsoNormal align=center style='text-align:center;text-indent:21.0pt'><b>总体方案设计</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　校园信息门户由门户界面管理<span lang=EN-US>.</span>统一身份认证服务和应用管理服务三部分组成，用户通过校园门户网站注册登录后，经统一身份认证，根据角色分别进入定制门户主页，其内容分别由信息服务系统<span
                        lang=EN-US>.</span>应用系统和网络基础服务提供。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　针对以上的应用，推荐采用云海高性能服务器<span lang=EN-US>NF8460M3.</span>高端企业级存储系统<span
                        lang=EN-US>AS510H</span>等设备，以满足与国际接轨的全日制普通高等学校的管理和教学需求。为更好地满足系统的安全需求和便于管理，推荐使用防火墙系统。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　考虑到系统的可靠性和稳定性，对于存储系统，建议在此系统采用冗余结构提升系统的整体可靠性，配置双控制器的在线全光纤盘阵和冗余光纤交换机。通过包括备份系统在内的数据保护系统，对在数据库业务系统运行平台上运行的各个应用系统提供了完整<span
                        lang=EN-US>.</span>可靠的数据保护。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　在该系统中除了服务器和存储系统外，系统网络安全也非常重要。要保证系统的安全性，不受到外部的攻击，必须要建设网络安全系统，网络安全系统建议采用千兆防火墙，从而最大限度的保证系统的安全性。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　通过集群技术，使得所有服务器构成一个有机的整体，建立了可以提供大容量接入能力<span lang=EN-US>.</span>大规模处理能力的应用支撑平台，满足了所运行的各个业务系统稳定<span
                        lang=EN-US>.</span>高效<span lang=EN-US>.</span>易管理的需求。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal><b>　　方案详细描述：</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>1.</span>统一身份认证服务（数据库系统）</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　根据经验估计，一个<span lang=EN-US>2</span>万人的学校<span lang=EN-US>,</span>对数据库服务器需求的<span
                        lang=EN-US>TPC-C</span>值大概在<span lang=EN-US>80</span>万左右。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　最低配置下，云海四路服务器的<span lang=EN-US>TPC-C</span>为<span
                        lang=EN-US>100</span>万以上，则在一台四路服务器<span lang=EN-US>NF8460M3</span>服务器上部署数据库模块，可以很好的满足性能需求<span
                        lang=EN-US>.</span>数据库通常是系统中最为宝贵的资源，数据库服务器一般要求高稳定性<span lang=EN-US>.</span>高处理能力，所以在<span
                        lang=EN-US>CPU.</span>内存等技术指标上尤其重要。根据业务压力和应用规模，数据库服务器选择如下：云海企业级四路服务器<span
                        lang=EN-US>NF8460M3</span>。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　云海<span lang=EN-US>NF8460M3</span>是云海公司根据数据库市场需求精心打造的，具有高性能<span
                        lang=EN-US>.</span>高可靠性<span lang=EN-US>.</span>高扩展性的专用硬件产品，每台服务器上配置双口<span
                        lang=EN-US>8GB FC HBA</span>卡，用以连接存储设备。立足于当前的发展，规划建设一个高效的<span lang=EN-US>.</span>可持续发展的数据库支撑平台，应该更多地关注平台的高可用性<span
                        lang=EN-US>.</span>可扩展性<span lang=EN-US>.</span>高度安全性，高可管理性等特性。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>2.</span>数据存储资源</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　系统设计考虑关键应用的可靠性和数据的安全性，在数据的可靠性方面采取了如下的措施：采用全冗余的<span
                        lang=EN-US>FC SAN</span>架构（<span lang=EN-US>2</span>块<span lang=EN-US>FC HBA</span>卡，<span
                        lang=EN-US>2</span>个光纤交换机以及冗余的磁盘阵列控制器）保证关键数据的可靠性；采用备份存储的方式使重要数据留有备份，当意外发生时，系统可以很快的恢复到正常状态，避免造成重大损失。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　其中主存储盘阵建议采用全光纤的<span lang=EN-US>AS510H</span>磁盘阵列， 同时配置<span
                        lang=EN-US>2</span>个控制器， 配置<span lang=EN-US>8</span>个<span lang=EN-US>16Gb</span>光纤接口，它所具有的超越同类产品的强大性能和超大扩展容量以及领先业界的数据管理<span
                        lang=EN-US>.</span>保护功能不仅有助于满足用户今天的各种存储需求，而且为满足未来存储需求的持续增长奠定了良好的基础，是一套高稳定<span
                        lang=EN-US>.</span>易管理<span lang=EN-US>.</span>方便扩展的可靠的高端企业级存储系统。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>3.</span>本地备份系统</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　备份系统是基于学校信息中心数据安全的需求，并针对当前环境的特点，所采取的多层次<span lang=EN-US>.</span>全方位的数据保护策略。备份子系统推荐采用云海虚拟带库<span
                        lang=EN-US>DP2000</span>和云海<span lang=EN-US>DPS</span>备份软件，能实现对当前业务数据库系统<span
                        lang=EN-US>.</span>文件系统等关键数据的备份，通过备份系统强大备份功能模块，采用高效的<span lang=EN-US>LAN-FREE</span>备份方式，将在线数据备份到云海虚拟带库。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　这样既可以利用高速的磁盘阵列提高备份效率<span lang=EN-US>.</span>缩小备份窗口，又可以充分利用近线的磁盘设备所带来的更高的安全性。备份系统可实现对操作系统<span
                        lang=EN-US>.</span>文件<span lang=EN-US>.</span>数据库的备份，充分数据的安全可靠。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>4.</span>应用管理服务（<span lang=EN-US>Web</span>系统）</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　门户子系统主要涉及学校的教务工作，是高校的核心工作。利用先进的技术手段和指导思想提高教育<span
                        lang=EN-US>.</span>培养<span lang=EN-US>.</span>管理水平，对提高人才的综合素质培养具有重大的影响。<span
                        lang=EN-US>Web</span>服务器是应用系统的信息发布平台。在应用系统的多层体系结构中，门户网站作为信息发布层，提供信息浏览<span
                        lang=EN-US>.</span>服务定位等功能。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>Web</span>服务要完成下列任务：</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　接收用户访问请求，并保持用户连接；将用户访问请求发送给应用服务，并等待应用服务的查询处理结果；接收应用服务的处理结果页面，提供给用户浏览。由此可见，<span
                        lang=EN-US>Web</span>服务器的系统资源主要用于满足对访问请求连接的处理，也就是服务器能够并行处理的<span lang=EN-US>Web</span>访问的数量。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　如果<span lang=EN-US>Web</span>服务主要为静态网页，内存数量是一个影响性能的关键因素，因为它决定了<span
                        lang=EN-US>Apache</span>可以缓存多少内容。它缓存的内容越多，在硬盘上读取内容的机会就越少，而存取硬盘上的特定文件是一件很费时的操作。另外，因为有大量的页面文件需要保存，因此需要一个相对较大的硬盘即可。如果<span
                        lang=EN-US>Web</span>服务主要为动态网页，并且将应用程序与发布系统分开，则其本身仍然类似于静态发布方式，只是由于需要增加一些系统资源，以便用于处理与应用<span
                        lang=EN-US>/</span>中间件服务器之间的通讯。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<span lang=EN-US>Web</span>服务器要处理大量的来自网络中的用户对<span
                        lang=EN-US>Web</span>站点的访问请求，这就需要一个强壮的<span lang=EN-US>CPU</span>来处理这些访问请求，并且需要足够大的内存来缓存尽可能多的访问请求，以提高系统的处理速度。而且要求磁盘<span
                        lang=EN-US>I/O</span>接口具有较大的通信带宽，同时网络线路具有较高的数据传输速率，以减少对网络用户访问请求的响应时间。因此<span
                        lang=EN-US>Web</span>服务器对资源的需求顺序依次为：<span lang=EN-US>CPU. </span>内存<span
                        lang=EN-US>.</span>磁盘<span lang=EN-US>I/O</span>。</p>

