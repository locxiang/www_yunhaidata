            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><strong><span lang=EN-US style='font-family:等线;
color:#333333'>1 </span></strong><strong><span style='font-family:等线;
color:#333333'>行业背景</span></strong></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　近年来各地持续加大公安信息化的建设力度，公安信息化建设取得了长足的发展，在基础设施建设、应用系统建设、数据资源开发利用、组织机构和工作机制建设、标准化建设等方面都取得了重大进展。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　近年来，随着公安大情报体系的建设，以及互联网技术的发展，一个以信息爆炸为特征的大数据时代正在到来。各种数据处理的技术层出不穷，这为<span
                            lang=EN-US>&quot;</span>数字警务<span lang=EN-US>&quot;</span>、<span lang=EN-US>&quot;</span>智慧警务<span
                            lang=EN-US>&quot;</span>带来了新的手段。对此，公安部门必须以创新的理念和思维，把深入实施科技强警战略，大力推进科技创新摆上更加重要的位置，努力提升公安工作的信息化、科学化和现代化水平。全警采集，内外联动，高度共享，综合应用。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　随着大数据技术的普及和应用，<span
                            lang=EN-US>&quot;</span>大情报体系<span lang=EN-US>&quot;</span>建设需要对数据的大开发，通过使用挖掘算法对海量数据进行分析和建模，挖掘出各类数据背后所蕴含的内在的、必然的因果关系，进而判断出某一事件发生的概率、科学预测其发展趋势，以此来服务打防管控等警务工作。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<strong><span
                                lang=EN-US style='font-family:等线'>2 </span></strong><strong><span
                                style='font-family:等线'>需求分析</span></strong></span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　当前公安的业务系统多样，但大多业务系统与实际的业务结合不够紧密，产生了技术与业务<span
                            lang=EN-US>&quot;</span>两张皮<span lang=EN-US>&quot;</span>的现象，进而信息化难以有效支撑业务的进展，而业务的演变难以对业务系统的进化形成促进，造成信息化与业务脱节。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　公安大情报体系建设需要大数据存储技术、管理技术、挖掘技术为契机，能够建立海量的公安数据资源库，不断挖掘公安情报，实现信息化随着业务的发展而快速变化，真正实现业务与信息化的融合，形成业务与信息化互相促进的格局。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<b>（一）<span
                                lang=EN-US> &nbsp;</span>建立大数据采集平台</b></span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　大数据采集平台大小取决于所获取的数据的广度与深度，所以我们要采集现有的各种数据，公安数据具体可以分为以下几类：一是公安基础工作数据信息。主要包括：人员信息、案件信息、物品信息、线索信息、场所信息等。二是公安内网共享的数据信息。主要包括公安部及各省市公安内网中的在逃人员信息、盗抢汽车信息、违法犯罪人员信息、法轮功信息、通缉令、预警信息、研判指令等。三是外部社会信息。主要包括互联网刊载的违法犯罪信息、商业网站的一些异常信息、保险理赔信息、个人资信信息、中介机构信息、检察机关案件信息、审判机关案件信息、信用卡组织信息、公证机构信息等。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<b>（二）<span
                                lang=EN-US> &nbsp;</span>建立大数据组织平台</b></span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　大数据采集平台的构建，解决了海量数据的统一存放问题，但是这些来自不同来源的公安数据仍然是散乱的、不规则的原始数据。对原始数据进行清洗、整合，建立规范、统一、完整的基础数据视图。然后在此基础上，针对业务部门提出的具体业务需求，进一步的进行数据的加工和处理，形成面向主题的数据集市，供业务部门使用。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<b>（三）<span
                                lang=EN-US> &nbsp;</span>建立大数据分析平台</b></span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　大数据分析是公安信息化应用的重要环节，也是情报信息再利用的基础。公安机关对大数据的分析解读是一个去伪存真、去粗取精的过程。分析解读的方法和内容主要是：一是对大数据信息真实性分析。违法犯罪活动大多处于隐蔽状态，所反映信息往往缺乏明朗性，有的甚至具有伪装性，对初期收集到的信息必须甄别其真伪；二是对大数据信息的比对查询分析。这是公安工作特别是侦查工作中运用较为广泛的分析解读手段，通过同类信息比对查询，扩展信息量，串并同类信息；三是量化比较分析。对某个区域、时段、案件、涉案人员、财物、场所以及线索等的数量与前期、上年同期、周边同期进行比较分析等等，例如包括信息查询、人员甄别、串并、碰撞比对，重大案件特点分析预警，各类数据的统计分析、规律提炼及情报收集，可疑人员身份锁定及人员控制，生物信息档案管理和证件鉴别研究等功能。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<strong><span
                                lang=EN-US style='font-family:等线'>3 </span></strong><strong><span
                                style='font-family:等线'>架构设计</span></strong></span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　该方案分<span
                            lang=EN-US>5</span>个层次，前端网络分流设备将多个业务系统和渠道接过来的数据流量接入到处理系统中，然后用软硬件分流器对数据流进行分析和过滤，它按照设定好的规则将数据进行分发和推送，这就是前端数据处理平台。数据接入层将前端处理完的各类数据转发到后端，由统一的数据抽取和转换平台对数据进行抽取和格式转换。通过数据抽取、转换和加载工具，将清洗好的数据加载到大数据平台中，由于汇聚了多个源的数据，通常数据量非常大，所以大数据平台需要具有良好的可扩展性。加载到大数据平台中的数据将被用户进行最终的数据分析和数据挖掘，同时用户还可以根据具体的分析和挖掘需求，设计自己的查询任务流及更多的应用系统。整体架构如下图所示：</span>
