                        style='font-family:"微软雅黑",sans-serif;color:#404040'>旅游产业涵盖较广，包括餐饮、酒店、交通、零售、娱乐等行业；</span></p>

            <p class=MsoNormal align=left style='margin-left:21.0pt;text-align:left'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>我国游客市场规模较大，游客时空变动性强，产生数据量较大；</span></p>

            <p class=MsoNormal align=left style='margin-left:21.0pt;text-align:left'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>旅游行业涉及到数据包括游客属性数据、交易数据、社交数据等，数据维度十分丰富。</span></p>

