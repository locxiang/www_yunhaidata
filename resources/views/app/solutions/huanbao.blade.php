            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><b><span style='font-family:等线;color:#333333'>一．背景分析</span></b></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;text-indent:24.0pt;background:white'><span style='font-family:等线;
color:#333333'>我国经济快速增长，各项建设取得巨大成就，但也付出了巨大的资源和环境代价，经济发展与资源环境的矛盾日趋尖锐，群众对环境污染问题反应强烈。这种状况与经济结构不合理、增长方式粗放直接相关。不加快调整经济结构、转变增长方式，资源支撑不住，环境容纳不下，社会承受不起，经济发展难以为继。只有坚持节约发展、清洁发展、安全发展，才能实现经济又好又快发展。同时，温室气体排放引起全球气候变暖，备受国际社会广泛关注。进一步加强节能减排工作，也是应对全球气候变化的迫切需要。</span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　为了确保主要污染物减排这一约束性目标的完成，迫切需要建立和完善科学的减排指标体系、准确的减排监测体系、严格的减排考核体系三大体系，从而通过严格科学的统计、监测和考核手段将减排工作落到实处。建设国控重点污染源自动监控系统，对占全国主要污染物负荷<span
                            lang=EN-US>65%</span>的国家重点监控企业的污染物排放情况实施自动监控是减排“三大体系”建设的重要组成部分，通过自动化、信息化等技术手段可以更加科学、准确、实时地掌握重点污染源的主要污染物排放数据、污染治理设施运行情况等与污染物排放相关的各类信息，及时发现并查处违法排污行为，是监督重点污染源是否完成污染物减排指标的有效手段。</span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><strong><span style='font-family:等线;color:#333333'>二、污染源自动监控系统项目建设内容</span></strong></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　污染源自动监控系统由自动监控设备和监控中心组成。自动监控设备是指在污染源现场安装的用于监控、监测污染物排放的仪器、流量（速）计、污染治理设施运行记录仪和数据采集传输仪等仪器、仪表。在线监测仪器能够监测污染源污染物数据浓度，流量（速）计能够连续不断的记录污染物流量，污染治理设施运行记录仪则能够实时监控排污企业污染治理设施的开关情况。</span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　监控中心是指环境保护部门通过通信传输线路与自动监控设备连接用于对重点污染源实施自动监控的软件和硬件，硬件主要包括服务器、污染源端数据接收专用设备、显示与交互系统、监控网络基础环境、网络安全系统等，软件由服务器操作系统与数据库软件（市售）、污染源基础数据库、污染源监控应用系统、数据传输与备份系统、网络安全系统等。</span></p>

