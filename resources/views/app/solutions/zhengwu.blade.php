        <p style="margin-left:3.75pt;background:white;">
            <strong>3</strong><strong>、政府大数据解决方案的价值</strong>
        </p>
        <p style="margin-left:48.55pt;text-indent:-21.0pt;background:white;">
            l&nbsp; 促进政府职能的转变<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;大数据的应用提高政府部门的办公效率，方便办公人员进行数据的共享并实现业务的办理。为打造创新型服务体系的新政府提供强有力的数据支撑。<span></span>
        </p>
        <p style="margin-left:48.55pt;text-indent:-21.0pt;background:white;">
            l&nbsp; 优化产业结构调整<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;基于数据标准建立大数据服务平台，挖掘大数据核心产业以及外围产业，将政府大数据相关产业资源进行整合形成政府大数据产业链，搭建政府大数据生态体系。<span></span>
        </p>
        <p style="margin-left:48.55pt;text-indent:-21.0pt;background:white;">
            l&nbsp; 促进民众信息消费<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;大数据提升城市设施智慧化水平，使民众生活日趋优质，通过民众大数据行为分析能为用户提供精准的信息服务。<span></span>
