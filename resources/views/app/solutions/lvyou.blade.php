    <h1>旅游大数据平台解决方案</h1>

    <div class="rowactiv-content">



        <div class=WordSection1 style='layout-grid:15.6pt'>

            <p class=MsoNormal align=left style='margin-left:0cm;text-align:left;
text-indent:0cm'><b><span lang=EN-US style='font-size:14.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>一、</span></b><b><span style='font-size:14.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>旅游行业概述</span></b></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>我国的旅游业正在获得前所未有的蓬勃发展，随着旅游消费观念的日益成熟，游客对体验的需求日益高涨，更渴望追求个性化、体验化、情感化、休闲化以及美化的旅游经历。</span></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>《“十三五”旅游业发展规划》中提到云计算、物联网、大数据等现代信息技术在旅游业的应用更加广泛，产业体系的现代化成为旅游业发展的必然趋势。同时对全国旅游规划提出了新的要求。</span></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><b><span
                            lang=EN-US style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>1.</span></b><b><span
                            style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>数据资源共享化</span></b></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>整合多元化的数据资源，经过清洗、转换，形成统一、标准的数据资源库，实现数据资源共享、互通、互联。</span></p>

            <p class=MsoNormal align=left style='margin-left:0cm;text-align:left;
text-indent:22.0pt'><b><span lang=EN-US style='font-size:11.0pt;font-family:
"微软雅黑",sans-serif;color:#404040'>2.</span></b><b><span style='font-size:11.0pt;
font-family:"微软雅黑",sans-serif;color:#404040'>产业运行数据化</span></b></p>

            <p class=MsoNormal align=left style='text-align:left'><span lang=EN-US
                                                                        style='font-family:"微软雅黑",sans-serif;color:#404040'>    </span><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>大力推动旅游科技创新，打造旅游发展科技引擎，建设旅游产业大数据平台。构建全国旅游产业运行监测平台，建立旅游与公安、交通、统计等部门数据共享机制，形成旅游产业大数据平台。</span></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><b><span
                            lang=EN-US style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>3.</span></b><b><span
                            style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>市场营销精准化</span></b></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>鼓励旅游目的地、旅游企业利用多渠道采集数据，实现市场营销精准化和产品与服务个性化推荐，提高市场营销的效能。</span></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><b><span
                            lang=EN-US style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>4.</span></b><b><span
                            style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>行业管理智能化</span></b></p>

            <p class=MsoNormal align=left style='text-align:left;text-indent:22.0pt'><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>提高旅游管理的科学化和智能化水平。积极推动政府相关部门数据资源开放和共享，建立旅游政务信息资源共享目录。</span></p>

            <p class=MsoNormal align=left style='margin-left:0cm;text-align:left;
text-indent:0cm'><b><span lang=EN-US style='font-size:14.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>二、</span></b><b><span style='font-size:14.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>旅游市场现状分析</span></b></p>

            <p class=MsoNormal align=left style='margin-left:21.0pt;text-align:left;
text-indent:0cm'><b><span lang=EN-US style='font-size:12.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>1.</span></b><b><span style='font-size:12.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>旅游市场增长较快，节假日景区拥堵严重</span></b></p>

            <p class=MsoNormal align=left style='text-align:left'><span lang=EN-US
                                                                        style='font-family:"微软雅黑",sans-serif;color:#404040'>    2016</span><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>年全年，中国公民出境旅游人数<span
                            lang=EN-US>1.22</span>亿人次，比上年同期增长<span lang=EN-US>4.3%</span>。出境旅游花费<span
                            lang=EN-US>1098</span>亿美元，比上年同期增长<span lang=EN-US>5.1%</span>。<span lang=EN-US>2017</span>年是实施“十三五”规划的重要一年，也是推进供给侧结构性改革的深化之年。我国旅游市场规模稳步扩大，旅游业在创新发展中继续领跑经济增长。</span></p>

            <p class=MsoNormal align=left style='margin-left:21.0pt;text-align:left;
text-indent:0cm'><b><span lang=EN-US style='font-size:12.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>2.</span></b><b><span style='font-size:12.0pt;font-family:"微软雅黑",sans-serif;
color:#404040'>各地旅游景点内旅游安全事件频发</span></b></p>

            <p class=MsoNormal align=left style='text-align:left'><span lang=EN-US
                                                                        style='font-family:"微软雅黑",sans-serif;color:#404040'>    </span><span
                        style='font-family:"微软雅黑",sans-serif;color:#404040'>国内旅游周期集中，游客密集，旅游意外事件时有发生。</span></p>

