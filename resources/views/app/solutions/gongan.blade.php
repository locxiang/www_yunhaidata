            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<b>数据源层：</b>分为业务数据和社会数据，这些数据基于网络通过数据接入层接入大数据平台。业务数据是公安行业内数据，包括人口数据，车辆数据，通信监控数据，刑侦数据等，社会数据是公安行业展开业务所需要的其他行业的数据。原始数据呈现的方式多种多样，它们可能来自数据库或者文件系统，采用各种不同的文件格式，取用不同的字符集编码。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><b><span style='font-family:等线;color:#333333'>　　数据接入层：</span></b><span
                        style='font-family:等线;color:#333333'>数据源层的数据海量，骨干网的流量非常大，须用网络分流设备进行分流。数据接入层通过网络分流设备将骨干网过来的网络流量接入到处理系统中，以加大海量数据并行处理能力，减轻骨干网的流量负担，增大<span
                            lang=EN-US>IO</span>操作的效率。</span></p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　数据清洗和转换：对接入的数据进行预处理和转换操作，以匹配定义的目标数据模式。数据预处理包括行列过滤，字段提取，文件拆分合并等；数据转换包括数据的格式转换，内容替换，对数据按照规则进行计算，列合并，列拆分，关联转换等。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><span style='font-family:等线;color:#333333'>　　<b>大数据平台：</b>提供结构化和非结构话数据的统一存储和处理，支持数据划分，索引，检索，统计分析。数据加载方面，可通过编程接口实现数据实时接入，通过加载工具工具由其他数据源（例如文件等）导入可实现海量数据的离线批量导入。数据查询方面，通过查询工具可实现数据的离线提取，为海量数据的线下分析提供条件；通过标准查询编程接口，可实现更加灵活可定制的数据提取方式；通过数据比对工具，为数据分析提供便利；查询结果可视化。</span>
            </p>

            <p style='margin-top:5.0pt;margin-right:3.75pt;margin-bottom:5.0pt;margin-left:
3.75pt;background:white'><b><span style='font-family:等线;color:#333333'>　　数据分析和挖掘：</span></b><span
                        style='font-family:等线;color:#333333'>为了分离业务开发与大数据平台，允许接入外部数据挖掘分析系统。在接入的方式上，可按照类标准<span
                            lang=EN-US>SQL</span>语句的方式，组织外部数据挖掘系统的数据请求；可采用查询工具或者查询编程接口，提取外务数据挖掘分析系统所需要的输入数据。</span></p>

