color:#3B3838'>1</span></b><b><span style='font-family:"微软雅黑",sans-serif;
color:#3B3838'>、决策支持</span></b></p>

            <p class=MsoNormal><b><span style='font-family:"微软雅黑",sans-serif;color:#3B3838'>数据支撑：</span></b><span
                        style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>对上千万篇裁判文书进行大数据分析，提取跨行政区划案件的数量及败诉率，并与未跨行政区划同类型案件的败诉率进行比对、建立模型。把每年数以千万计的案件数据放入数据模型中进行计算，就可以实时监控跨行政区划案件是否存在地方保护情况。</span></p>

            <p class=MsoNormal><b><span style='font-family:"微软雅黑",sans-serif;color:#3B3838'>精准推送：</span></b><span
                        style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>对裁判文书中的海量数据进行提炼、分析，智能并精准地为检察官和法官主动推送与其在办案件密切相关的法律法规、相似案件的裁判文书等信息。</span></p>

            <p class=MsoNormal><b><span style='font-family:"微软雅黑",sans-serif;color:#3B3838'>辅助决策：</span></b><span
                        style='font-size:11.0pt;font-family:"微软雅黑",sans-serif;color:#404040'>对不同数据源的数据进行比对、关联、分析和预测，对国家公职人员履行职务期间碎片化电子数据全体样本进行相关性分析，发现线索，辅助决策。</span></p>

