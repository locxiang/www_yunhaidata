<div class="rowactive bg-color">
    <h1>政务大数据解决方案</h1>

    <div class="rowactiv-content">
        <p style="margin-left:3.75pt;background:white;">
            &emsp;<b>&emsp;政府大数据机遇<span></span></b>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;“政府”和“大数据”的结合成为历史发展的新趋势，政府大数据技术建构和数据应用进入了启动期，技术的研发，应用的创新，管理的调整，理念的学习，成为政府大数据发展的关键词。<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;<b>政府大数据方向</b><span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;● 以数据为中心构建政府应用系统<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;● 跨行业、跨领域的综合数据分析与预测<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;● 让数据成为真正的资源，可以交易<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;● 利用大数据为政府创造价值<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;● 依托大数据技术改变政府服务方式<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            <b>&emsp;&emsp;政府大数据挑战<span></span></b>
        </p>
        <p style="margin-left:48.55pt;text-indent:-21.0pt;background:white;">
            l&nbsp; 数据难采集<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;数据主要用于统计，大量数据丢失，重视结果，不重视数据<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;孤岛现象突出，数据很难有效共享<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;数据良莠不齐、质量低，数据格式不统一<span></span>
        </p>
        <p style="margin-left:48.55pt;text-indent:-21.0pt;background:white;">
            l&nbsp; 数据难管理<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;组织<span>/</span>行业政策上的限制<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;缺乏激励和规则，数据共享多限于内部，制约其价值实现<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;数据的可靠性和安全要求高，数据共享与隐私的矛盾等问题顾虑重重<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;不同数据的混合存储，数据分析复杂度提升<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            <strong>2</strong><strong>、云海政府大数据解决方案</strong>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;基于大数据共享服务平台提供多种服务能力，涵盖了政府大数据产业规划顶层设计、大数据标准的咨询、大数据产业链的整合、以及大数据生态的构建在内的各个方面，从政府数据仓库到大数据存储与分析平台，形成一个完整的大数据应用支撑体系。<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;政府大数据港解决方案：主要围绕着一个核心、两条主线、三种模式、四项任务和五大意义这五个维度进行展开，秉着“标准先行，技术保障，业务驱动”的原则进行规划。<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;一个核心：<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;始终围绕盘点和整合数据资源为核心<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;两条主线：<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;构建对内对外两条服务主线；对内，构建数据资产盘点标准和资产管理体系；对外：提供数据共享开放服务能力（有限开放、完全开放）<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;三种模式：<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;按照电子政务系统等级保护的要求，提供数据资源内港、外港和互联港三种服务模式；其中，内港：内部使用，不提供共享，内部盘点，统计；外港：本市使用，区域内共享；互联港：全部公开，服务大众创业，万众创新；满足不同层级的要求。<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;四项任务：<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;数据资源编目体系和盘点标准研究，数据接入整合能力建设，数据清洗脱敏处理体系建设，数据应用服务体系建设；<span></span>
        </p>
        <p style="margin-left:3.75pt;background:white;">
            &emsp;&emsp;五大意义：<span></span>
        </p>
        <p style="margin-left:39.75pt;text-indent:-18.0pt;background:white;">
            1. 数据资产明晰化；<span>2.</span>数据管理规范化；<span>3.</span>数据共享联动化；<span>4.</span>数据保障体系化；<span>5.</span>数据应用普适化；<span></span>
        </p>
