            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal><b>一 数字校园概念</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　在<span lang=EN-US>20</span>世纪末提出<span lang=EN-US>&quot;</span>数字校园<span
                        lang=EN-US>&quot;</span>概念以来的十几年中，数字校园经过高校信息化建设的不断摸索，逐渐的发展起来，目前已进入快速发展期。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal style='text-indent:21.0pt'>信息技术的飞速发展为高校信息化建设提供了机遇和条件，也不断暴露出很多新问题，如信息孤岛<span
                        lang=EN-US>.</span>信息安全等问题导致业务流程不通畅<span lang=EN-US>.</span>用户使用不方便<span
                        lang=EN-US>.</span>系统应用推广难等等。随着应用系统的增多，经常需要牵扯到其它应用和流程的改造，以便进行数据共享<span
                        lang=EN-US>.</span>交换和更新。这就需要建设一个统一的信息访问平台。</p>

            <p class=MsoNormal align=left style='text-align:left'><span lang=EN-US><img
                            width=415 height=214 id="图片 8" src="http://www.tiaozhanbei.net/media/pro_project/proimage/2011/7/26/cache/36913d81-23d0-4906-b56f-c55b99589d47_display.jpg"
                            alt="http://www.tiaozhanbei.net/media/pro_project/proimage/2011/7/26/cache/36913d81-23d0-4906-b56f-c55b99589d47_display.jpg"></span></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　数字校园是一个广范围概念，它包括了现实校园及数字化空间，也包含了当前流行的虚拟大学。现实校园是数字校园的基础，数字校园是现实校园通过信息技术在时间和空间上的扩展与延伸，它包含了现实校园及其所衍生出来的数字空间，虚拟大学是数字校园的远程教育功能部分，是数字校园的对外服务的部分职能，它是传统校园数字化后社会功能的延伸。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　数字校园是利用计算机技术<span lang=EN-US>.</span>网络技术<span lang=EN-US>.</span>通信技术对学校与教学<span
                        lang=EN-US>.</span>科研<span lang=EN-US>.</span>管理和生活服务有关的所有信息资源进行全面的数字化；并用科学规范的管理对这些信息资源进行整合和集成，以构成统一的用户管理<span
                        lang=EN-US>.</span>统一的资源管理和统一的权限控制；把学校建设成面向校园内，也面向社会的一个超越时间和空间的虚拟大学。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　数字校园是以网络为基础，从环境<span lang=EN-US>.</span>资源<span
                        lang=EN-US>.</span>到活动的全部数字化校园网络及其应用系统构成整个校园的神经系统，完成校园的信息传递和服务。在数字校园里，可以通过现代化手段，方便地实现学校的教学<span
                        lang=EN-US>.</span>科研<span lang=EN-US>.</span>管理<span lang=EN-US>.</span>服务等活动的全部过程，从而达到提高传统校园效率，扩展传统校园功能，最终实现教育过程的全面信息化，达到提高教育水平<span
                        lang=EN-US>.</span>管理效率的目的。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal><b>二 校园门户系统需求与分析</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　校园门户服务是数字校园的高级表现形式，凌驾于各类应用之上，作为数字校园窗口，以浏览器的方式向用户展现数字校园的应用信息，针对不同的用户，为他们提供基于个人访问权限的个性化界面和服务。为全校师生员工及校外用户提供快捷<span
                        lang=EN-US>.</span>共享<span lang=EN-US>.</span>全面的信息服务。为高校实现<span lang=EN-US>&quot;</span>网上教学<span
                        lang=EN-US>.</span>网上科研<span lang=EN-US>.</span>网上管理<span lang=EN-US>.</span>网上服务<span
                        lang=EN-US>.</span>网上办公<span lang=EN-US>&quot;</span>。对学校而言，校园门户是对外的窗口，是宣传形象的载体。对老师<span
                        lang=EN-US>.</span>学生和学校管理人员而言，校园门户是科研<span lang=EN-US>.</span>学习和管理的平台。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　门户服务独立于各类应用之上，作为高校对内外的窗口，以浏览器的方式向用户展现数字化校园的应用信息，通过人员整合（组织机构<span
                        lang=EN-US>.</span>用户管理），使得学院用户（教师<span lang=EN-US>.</span>学生<span lang=EN-US>.</span>职工<span
                        lang=EN-US>.</span>校外人员等）自由定制个性化的信息内容。此外，门户的另一用途就是应用整合，提供各种服务及应用的接入，如<span
                        lang=EN-US>E-mail</span>系统<span lang=EN-US>.</span>教务管理<span lang=EN-US>.</span>办公系统<span
                        lang=EN-US>.</span>校园虚拟社区<span lang=EN-US>.</span>主页托管系统<span lang=EN-US>.</span>信息发布系统等等。</p>

