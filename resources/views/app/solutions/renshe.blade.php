            <p class=MsoNormal style='text-indent:22.0pt'><span style='font-size:11.0pt;
font-family:"微软雅黑",sans-serif;color:#404040'>目前，人社部门做大数据主要面临的问题有：大数据意识不够、大数据发展管理机制体制不健全、业务和信息的融合度不深、信息采集和治理能力不足。</span></p>

            <p class=MsoNormal style='text-indent:22.0pt'><span style='font-size:11.0pt;
font-family:"微软雅黑",sans-serif;color:#404040'>在大数据国家重大战略的推动下，有助于加快人力资源和社会保障领域简政放权、放管结合、优化服务改革，对于增强社会保障工作效能，提升公共服务水平和能力，具有重要意义。</span></p>

