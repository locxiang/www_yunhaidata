            <p class=MsoNormal align=center style='text-align:center'>校园门户框图</p>

            <p class=MsoNormal>　　<b>当前校园门户存在的问题：</b></p>

            <p class=MsoNormal><b><span lang=EN-US>&nbsp;</span></b></p>

            <p class=MsoNormal><b>　　<span lang=EN-US>1.</span>普及率不高</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　各种教育机构的门户平台建设都存在不足。即使在信息化水平较高的高等教育，各学科<span lang=EN-US>.</span>院系<span
                        lang=EN-US>.</span>专业的门户建设水平也参差不齐。</p>

            <p class=MsoNormal><b><span lang=EN-US>&nbsp;</span></b></p>

            <p class=MsoNormal><b>　　<span lang=EN-US>2.</span>各个系统相互独立</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　按现在的应用模式，校园网的各个应用系统之间基本上是独立的，都有各自独立的用户管理和认证模块，这样就导致了应用系统之间的用户信息可能不同，给用户在使用上带来了极大的不便，使用户面对多个系统要重复输入账号<span
                        lang=EN-US>.</span>口令等信息，不仅烦琐，既给用户带来使用不便，也存在严重的安全隐患。同一教育结构内各个部门间的门户平台<span
                        lang=EN-US>,</span>网站从技术架构<span lang=EN-US>.</span>结构<span lang=EN-US>.</span>风格等方面看都缺乏一致性，难以支持统一形象的展示和统一管理<span
                        lang=EN-US>.</span>服务的实现。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>3.</span>设计实现技术落后</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　很多门户平台采用技术落后，在管理<span lang=EN-US>.</span>维护<span
                        lang=EN-US>.</span>更新等方面造成了障碍，不利于提高网站的被搜寻成功率，也不利于实现网站的宣传功能。也无法实现统一机构内多级门户体系的构建和管理维护。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　<b><span lang=EN-US>4.</span>系统安全性低</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　技术的落后和安全体系的缺失使得教育机构门户平台往往成为病毒<span lang=EN-US>.</span>木马和黑客的目标。实现单点登录的方式，具有较高安全控制的统一身份认证系统，以保证数据一致<span
                        lang=EN-US>.</span>安全<span lang=EN-US>.</span>使用和管理方便。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　<b>　<span lang=EN-US>5.</span>系统更新周期长</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal>　　技术和运维的局限使得门户内容的统一管理<span lang=EN-US>.</span>更新<span
                        lang=EN-US>.</span>权限控制和展示手段都无法支撑使用过的需求。内外网门户资源不够统一，内容老化，信息发布不同步，信息资源的检索及利用率不高，缺乏个性化的自定义。因此需要对分散在各地的用户实行安全管理及个性化服务。并支持后续协同其他相关部门的业务网站和应用系统。实现资源整合。在减少重复投资的前提下，提供相关的增值服务。</p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal><b>三 云海校园门户解决方案</b></p>

            <p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

            <p class=MsoNormal><span lang=EN-US><img width=415 height=240 id="图片 9"
                                                     src="http://www.uwaysoft.com/uwaysoft/images/20120926/xiaoyuanwang_20120926181135.png"
